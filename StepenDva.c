#include <stdio.h>
int main(void) { int n; scanf("%d", &n); // Читаем число
    
    // 0 не является степенью двойки
    if (n == 0) { printf("NO\n"); return 0;
    }
    
    // Главный цикл проверки Пока число делится на 2 без остатка
    while (n % 2 == 0) { n = n / 2; // Делим на 2 printf("%d\n",n);
    }
    
    // Если после всех делений получилась 1 - это степень двойки (2⁰=1, 
    // 2¹=2→1, 2²=4→2→1 и т.д.)
    if (n == 1) { printf("YES\n");
    } else {
        printf("NO\n");
    }
    
    return 0;
}
